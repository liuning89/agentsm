package com.lifang.housesoa.model;

public class HouseResourceConcact {
    private Integer houseId;
    private String hostMobile;
    private String hostName;
    private Integer houseState;
    private Integer companyId;
    private String province;
    private Integer provinceid;
    private String city;
    private Integer cityid;

    public HouseResourceConcact() {}
    public Integer getHouseId() { return houseId; }
    public void setHouseId(Integer houseId) { this.houseId = houseId; }
    public String getHostMobile() { return hostMobile; }
    public void setHostMobile(String hostMobile) { this.hostMobile = hostMobile; }
    public String getHostName() { return hostName; }
    public void setHostName(String hostName) { this.hostName = hostName; }
    public Integer getHouseState() { return houseState; }
    public void setHouseState(Integer houseState) { this.houseState = houseState; }
    public Integer getCompanyId() { return companyId; }
    public void setCompanyId(Integer companyId) { this.companyId = companyId; }
    public String getProvince() { return province; }
    public void setProvince(String province) { this.province = province; }
    public Integer getProvinceid() { return provinceid; }
    public void setProvinceid(Integer provinceid) { this.provinceid = provinceid; }
    public String getCity() { return city; }
    public void setCity(String city) { this.city = city; }
    public Integer getCityid() { return cityid; }
    public void setCityid(Integer cityid) { this.cityid = cityid; }
}
