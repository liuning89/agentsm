package com.lifang.imgsoa.model;

public class ImageKeyObject {
    private String key;
    private String original;
    private String url;
    private String water_cl;
    private String water_ml;
    private String water_al;
    private String water_dl;
    private String water_dj;

    public ImageKeyObject() {}
    public String getKey() { return key; }
    public void setKey(String key) { this.key = key; }
    public String getOriginal() { return original; }
    public void setOriginal(String original) { this.original = original; }
    public String getUrl() { return url; }
    public void setUrl(String url) { this.url = url; }
    public String getWater_cl() { return water_cl; }
    public void setWater_cl(String water_cl) { this.water_cl = water_cl; }
    public String getWater_ml() { return water_ml; }
    public void setWater_ml(String water_ml) { this.water_ml = water_ml; }
    public String getWater_al() { return water_al; }
    public void setWater_al(String water_al) { this.water_al = water_al; }
    public String getWater_dl() { return water_dl; }
    public void setWater_dl(String water_dl) { this.water_dl = water_dl; }
    public String getWater_dj() { return water_dj; }
    public void setWater_dj(String water_dj) { this.water_dj = water_dj; }
}
